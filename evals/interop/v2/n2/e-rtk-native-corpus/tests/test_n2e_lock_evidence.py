"""Independent Cargo.lock evidence derivation (follow-up items 1/2/4): the retained
A-/B-Cargo.lock bytes are re-parsed by the verifier, the full package metadata is derived from
that parse (not trusted from a producer list+hash), cross-checked against post_install_state
and the resolved snapshot, and coordinated tampers must all fail. The host resolve graph is
structurally host-only (no packages array)."""
import hashlib
import json
import sys
import tempfile
import unittest
from pathlib import Path

N2E_DIR = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(N2E_DIR / "tools"))
import probe_coreutils_diagnostic as P  # noqa: E402
import verify_coreutils_diagnostic as V  # noqa: E402

LOCK_A = (b'# This file is automatically @generated by Cargo.\n'
          b'version = 3\n\n'
          b'[[package]]\nname = "libc"\nversion = "0.2.159"\n'
          b'source = "registry+https://github.com/rust-lang/crates.io-index"\n'
          b'checksum = "' + b"a" * 64 + b'"\n')
HOST_GRAPH = {"resolve_roots": ["coreutils 0.0.27"],
              "resolve_nodes": [{"id": "coreutils 0.0.27", "deps": [{"pkg": "libc 0.2.159"}]},
                                {"id": "libc 0.2.159", "deps": []}],
              "reachable_package_ids": ["coreutils 0.0.27", "libc 0.2.159"],
              "resolve_graph_platform": "x86_64-unknown-linux-gnu", "resolve_graph_scope": "host-filtered"}


def _ok_fetch(raw):
    return {"status": "ok",
            "dependency_fetch": {"argv": ["cargo", "fetch", "--locked"],
                                 "env": {"RUSTUP_TOOLCHAIN": "1.81.0", "CARGO_NET_OFFLINE": "false"},
                                 "exit": 0, "timed_out": False, "stdout_sha256": "0" * 64,
                                 "stderr_sha256": "0" * 64, "stderr_tail": ""},
            "pre_fetch_lock_sha256": _sha(raw), "pre_fetch_lock_bytes": len(raw),
            "post_fetch_lock_sha256": _sha(raw), "post_fetch_lock_bytes": len(raw)}


def _sha(b):
    return hashlib.sha256(b).hexdigest()


def _build(evidence, lock_a=LOCK_A, lock_b=LOCK_A):
    """Write a consistent A/B evidence set and return the matching producer record."""
    def acq(raw):
        pkgs = P._normalize_lock_packages(raw)
        rds = {"cargo_lock_sha256": _sha(raw), "cargo_lock_bytes": len(raw),
               "cargo_lock_scope": "full cross-platform resolution",
               "full_packages_metadata": pkgs, "full_packages_metadata_sha256": P._manifest_hash(pkgs),
               "full_package_count": len(pkgs),
               "host_resolve_graph": HOST_GRAPH, "host_resolve_graph_sha256": P._manifest_hash(HOST_GRAPH),
               "host_resolved_package_count": len(HOST_GRAPH["reachable_package_ids"]), "metadata_ok": True}
        priv = {"_cargo_cache_manifests": {"full": {}, "semantic": {}, "validator": {}, "unparseable": [],
                                           "payloads": {}, "revisions": {}},
                "resolved_dependency_snapshot": rds, "_cargo_lock_raw": raw,
                "post_install_state": {"cargo_lock": {"present": True, "sha256": _sha(raw), "bytes": len(raw)}},
                "dependency_fetch_result": _ok_fetch(raw),
                "install": {"exit": 0, "timed_out": False}}
        return priv
    A, B = acq(lock_a), acq(lock_b)
    P._write_cargo_cache_evidence(A, B, evidence)
    rec = {"acquisition_A": {k: v for k, v in A.items() if not k.startswith("_")},
           "acquisition_B": {k: v for k, v in B.items() if not k.startswith("_")}}
    return rec


class TestLockEvidence(unittest.TestCase):
    def setUp(self):
        self.ev = Path(tempfile.mkdtemp())

    def test_consistent_passes(self):
        rec = _build(self.ev)
        fail = []
        v = V._verify_resolved_graph_evidence(rec, self.ev, fail)
        self.assertEqual(fail, [], fail)
        self.assertTrue(v["graph_equal"] and v["full_pkgs_equal"] and v["lock_equal"] and v["lock_present"])

    def test_missing_A_lock_fails(self):
        rec = _build(self.ev)
        (self.ev / "cargo-cache" / "A-Cargo.lock").unlink()
        fail = []
        v = V._verify_resolved_graph_evidence(rec, self.ev, fail)
        self.assertTrue(any("A-Cargo.lock missing" in f for f in fail))
        self.assertFalse(v["lock_present"])

    def test_missing_B_lock_fails(self):
        rec = _build(self.ev)
        (self.ev / "cargo-cache" / "B-Cargo.lock").unlink()
        fail = []
        v = V._verify_resolved_graph_evidence(rec, self.ev, fail)
        self.assertTrue(any("B-Cargo.lock missing" in f for f in fail))
        self.assertFalse(v["lock_present"])

    def test_lock_bytes_changed_hash_unchanged_fails(self):
        # tamper the retained A-Cargo.lock bytes but leave the recorded hash -> sha mismatch
        rec = _build(self.ev)
        (self.ev / "cargo-cache" / "A-Cargo.lock").write_bytes(LOCK_A + b"# tampered\n")
        fail = []
        V._verify_resolved_graph_evidence(rec, self.ev, fail)
        self.assertTrue(any("retained lock sha != post_install_state" in f or
                            "retained lock sha != resolved_dependency_snapshot" in f for f in fail))

    def test_lock_bytes_and_hash_changed_together_fails(self):
        # A's lock legitimately re-hashed but now differs from B -> A==B parity breaks
        alt = LOCK_A.replace(b"0.2.159", b"0.2.160")
        rec = _build(self.ev, lock_a=alt, lock_b=LOCK_A)
        fail = []
        v = V._verify_resolved_graph_evidence(rec, self.ev, fail)
        self.assertFalse(v["lock_equal"])
        self.assertTrue(any("retained Cargo.lock bytes differ" in f for f in fail))

    def test_full_package_list_changed_with_hash_fails(self):
        # rewrite A's resolved-graph JSON full package list + its sha, but the retained lock is
        # unchanged -> the lock-DERIVED package sha disagrees with the retained list
        rec = _build(self.ev)
        gp = self.ev / "cargo-cache" / "A-resolved-graph.json"
        data = json.loads(gp.read_text())
        data["full_packages_metadata"] = [{"name": "evil", "version": "9.9.9", "source": None, "checksum": None}]
        data["full_packages_metadata_sha256"] = P._manifest_hash(data["full_packages_metadata"])
        gp.write_text(json.dumps(data, sort_keys=True, indent=1))
        fail = []
        V._verify_resolved_graph_evidence(rec, self.ev, fail)
        self.assertTrue(any("retained package list != lock-derived" in f or
                            "lock-derived package sha != retained resolved-graph record" in f for f in fail))

    def test_record_lock_hash_disagrees_with_post_install_fails(self):
        rec = _build(self.ev)
        rec["acquisition_A"]["post_install_state"]["cargo_lock"]["sha256"] = "d" * 64
        fail = []
        V._verify_resolved_graph_evidence(rec, self.ev, fail)
        self.assertTrue(any("retained lock sha != post_install_state" in f for f in fail))

    def test_host_graph_with_packages_array_fails(self):
        # item 4: the host graph must not embed a top-level packages array
        rec = _build(self.ev)
        gp = self.ev / "cargo-cache" / "A-resolved-graph.json"
        data = json.loads(gp.read_text())
        data["host_resolve_graph"]["packages"] = [{"id": "libc"}]
        data["host_resolve_graph_sha256"] = P._manifest_hash(data["host_resolve_graph"])
        gp.write_text(json.dumps(data, sort_keys=True, indent=1))
        fail = []
        V._verify_resolved_graph_evidence(rec, self.ev, fail)
        self.assertTrue(any("host graph keys" in f for f in fail))


class TestDependencyFetchEvidence(unittest.TestCase):
    """item 3: independent dependency-fetch verification (exact argv/env, exit0, not timed_out,
    pre==post==retained lock). Each tamper must set dependency_fetch_ok / fetch_lock_unchanged
    false and append a failure."""

    def setUp(self):
        self.ev = Path(tempfile.mkdtemp())

    def _run(self, mutate):
        rec = _build(self.ev)
        mutate(rec["acquisition_A"]["dependency_fetch_result"])
        fail = []
        v = V._verify_resolved_graph_evidence(rec, self.ev, fail)
        return v, fail

    def test_consistent_fetch_ok(self):
        rec = _build(self.ev)
        fail = []
        v = V._verify_resolved_graph_evidence(rec, self.ev, fail)
        self.assertTrue(v["dependency_fetch_ok"] and v["fetch_lock_unchanged"])
        self.assertEqual([f for f in fail if "dependency-fetch" in f], [])

    def test_wrong_argv_fails(self):
        v, fail = self._run(lambda d: d["dependency_fetch"].update(argv=["cargo", "fetch"]))
        self.assertFalse(v["dependency_fetch_ok"])
        self.assertTrue(any("argv" in f for f in fail))

    def test_wrong_env_fails(self):
        v, fail = self._run(lambda d: d["dependency_fetch"].update(env={"CARGO_NET_OFFLINE": "true"}))
        self.assertFalse(v["dependency_fetch_ok"])
        self.assertTrue(any("env" in f for f in fail))

    def test_nonzero_exit_fails(self):
        v, fail = self._run(lambda d: d["dependency_fetch"].update(exit=101))
        self.assertFalse(v["dependency_fetch_ok"])
        self.assertTrue(any("exit != 0" in f for f in fail))

    def test_timed_out_fails(self):
        v, fail = self._run(lambda d: d["dependency_fetch"].update(timed_out=True))
        self.assertFalse(v["dependency_fetch_ok"])
        self.assertTrue(any("timed_out" in f for f in fail))

    def test_status_not_ok_fails(self):
        v, fail = self._run(lambda d: d.update(status="COREUTILS_DEPENDENCY_FETCH_FAILURE"))
        self.assertFalse(v["dependency_fetch_ok"])
        self.assertTrue(any("status != ok" in f for f in fail))

    def test_lock_mutation_fails(self):
        v, fail = self._run(lambda d: d.update(post_fetch_lock_sha256="f" * 64))
        self.assertFalse(v["fetch_lock_unchanged"])
        self.assertTrue(any("fetch mutated the lock" in f or "post-fetch lock" in f for f in fail))


class TestHostGraphValidation(unittest.TestCase):
    """item 4: exact platform/scope + exact structural keys + independently-recomputed
    reachable set. Each tamper (with the graph sha recomputed to isolate the structural check)
    must fail."""

    def setUp(self):
        self.ev = Path(tempfile.mkdtemp())

    def _tamper_graph(self, mutate_graph=None, mutate_data=None):
        rec = _build(self.ev)
        gp = self.ev / "cargo-cache" / "A-resolved-graph.json"
        data = json.loads(gp.read_text())
        if mutate_graph:
            mutate_graph(data["host_resolve_graph"])
            data["host_resolve_graph_sha256"] = P._manifest_hash(data["host_resolve_graph"])
        if mutate_data:
            mutate_data(data)
        gp.write_text(json.dumps(data, sort_keys=True, indent=1))
        fail = []
        V._verify_resolved_graph_evidence(rec, self.ev, fail)
        return fail

    def test_missing_platform(self):
        fail = self._tamper_graph(lambda g: g.pop("resolve_graph_platform"))
        self.assertTrue(any("resolve_graph_platform" in f for f in fail))

    def test_missing_scope(self):
        fail = self._tamper_graph(lambda g: g.pop("resolve_graph_scope"))
        self.assertTrue(any("resolve_graph_scope" in f for f in fail))

    def test_wrong_platform(self):
        fail = self._tamper_graph(lambda g: g.update(resolve_graph_platform="aarch64-apple-darwin"))
        self.assertTrue(any("resolve_graph_platform != x86_64-unknown-linux-gnu" in f for f in fail))

    def test_wrong_scope(self):
        fail = self._tamper_graph(lambda g: g.update(resolve_graph_scope="all-platforms"))
        self.assertTrue(any("resolve_graph_scope != host-filtered" in f for f in fail))

    def test_tampered_reachable_with_updated_hash(self):
        fail = self._tamper_graph(lambda g: g.update(reachable_package_ids=["coreutils 0.0.27"]))
        self.assertTrue(any("recorded reachable_package_ids != recomputed" in f for f in fail))

    def test_tampered_package_count(self):
        fail = self._tamper_graph(mutate_data=lambda d: d.update(host_resolved_package_count=99))
        self.assertTrue(any("host_resolved_package_count" in f for f in fail))

    def test_unreachable_node_inserted_into_reachable(self):
        fail = self._tamper_graph(lambda g: g.update(
            reachable_package_ids=sorted(g["reachable_package_ids"] + ["ghost 9.9.9"])))
        self.assertTrue(any("recorded reachable_package_ids != recomputed" in f for f in fail))

    # ---- item C: explicit resolve_roots + structural validation ----
    def test_empty_roots(self):
        fail = self._tamper_graph(lambda g: g.update(resolve_roots=[]))
        self.assertTrue(any("resolve_roots empty" in f for f in fail))

    def test_root_not_a_node(self):
        fail = self._tamper_graph(lambda g: g.update(resolve_roots=["ghost 9.9.9"]))
        self.assertTrue(any("not present in resolve_nodes" in f for f in fail))

    def test_duplicate_node_ids(self):
        fail = self._tamper_graph(lambda g: g["resolve_nodes"].append({"id": "libc 0.2.159", "deps": []}))
        self.assertTrue(any("duplicate node ids" in f for f in fail))

    def test_dangling_dependency_edge(self):
        # libc now points at a package that is not a node -> dangling edge
        fail = self._tamper_graph(lambda g: g["resolve_nodes"].__setitem__(
            1, {"id": "libc 0.2.159", "deps": [{"pkg": "ghost 9.9.9"}]}))
        self.assertTrue(any("dangling dependency edge" in f for f in fail))

    def test_disconnected_injected_node(self):
        # an orphan node reachable from no root inflates the node set but not reachability
        fail = self._tamper_graph(lambda g: g["resolve_nodes"].append({"id": "orphan 1.0.0", "deps": []}))
        self.assertTrue(any("disconnected from resolve_roots" in f for f in fail))


class TestMandatorySnapshotFields(unittest.TestCase):
    """item B: every resolved-snapshot field is MANDATORY for a
    publisher_install_resolved_dependency_snapshot -- there is no 'None is acceptable' path. A
    coordinated deletion of any single field across A and B (so A/B equality cannot mask it) must
    set lock_present false and append a 'missing'/scope failure."""

    def setUp(self):
        self.ev = Path(tempfile.mkdtemp())

    def _verify(self, rec):
        fail = []
        v = V._verify_resolved_graph_evidence(rec, self.ev, fail)
        return v, fail

    def _edit_graph(self, mutate):
        for label in ("A", "B"):
            gp = self.ev / "cargo-cache" / f"{label}-resolved-graph.json"
            data = json.loads(gp.read_text())
            mutate(data)
            gp.write_text(json.dumps(data, sort_keys=True, indent=1))

    def _del_rds(self, rec, key):
        for lbl in ("A", "B"):
            rec[f"acquisition_{lbl}"]["resolved_dependency_snapshot"].pop(key, None)

    def test_missing_post_install_cargo_lock_bytes(self):
        rec = _build(self.ev)
        for lbl in ("A", "B"):
            del rec[f"acquisition_{lbl}"]["post_install_state"]["cargo_lock"]["bytes"]
        v, fail = self._verify(rec)
        self.assertFalse(v["lock_present"])
        self.assertTrue(any("post_install_state.cargo_lock.bytes missing" in f for f in fail))

    def test_missing_resolved_snapshot_cargo_lock_bytes(self):
        rec = _build(self.ev)
        self._del_rds(rec, "cargo_lock_bytes")
        v, fail = self._verify(rec)
        self.assertFalse(v["lock_present"])
        self.assertTrue(any("resolved_dependency_snapshot.cargo_lock_bytes missing" in f for f in fail))

    def test_missing_retained_full_packages_sha(self):
        rec = _build(self.ev)
        self._edit_graph(lambda d: d.pop("full_packages_metadata_sha256", None))
        v, fail = self._verify(rec)
        self.assertFalse(v["lock_present"])
        self.assertTrue(any("retained full_packages_metadata_sha256 missing" in f for f in fail))

    def test_missing_producer_full_packages_sha(self):
        rec = _build(self.ev)
        self._del_rds(rec, "full_packages_metadata_sha256")
        v, fail = self._verify(rec)
        self.assertFalse(v["lock_present"])
        self.assertTrue(any("producer full_packages_metadata_sha256 missing" in f for f in fail))

    def test_missing_host_resolved_package_count(self):
        rec = _build(self.ev)
        self._edit_graph(lambda d: d.pop("host_resolved_package_count", None))
        self._del_rds(rec, "host_resolved_package_count")
        v, fail = self._verify(rec)
        self.assertFalse(v["lock_present"])
        self.assertTrue(any("host_resolved_package_count missing" in f for f in fail))

    def test_missing_cargo_lock_scope(self):
        rec = _build(self.ev)
        self._edit_graph(lambda d: d.pop("cargo_lock_scope", None))
        self._del_rds(rec, "cargo_lock_scope")
        v, fail = self._verify(rec)
        self.assertFalse(v["lock_present"])
        self.assertTrue(any("cargo_lock_scope" in f for f in fail))


class TestReachableIds(unittest.TestCase):
    def test_reachability_from_root(self):
        nodes = [{"id": "root", "deps": [{"pkg": "a"}]}, {"id": "a", "deps": [{"pkg": "b"}]},
                 {"id": "b", "deps": []}, {"id": "orphan", "deps": []}]
        r = P._reachable_ids(["root"], nodes)
        self.assertEqual(r, ["a", "b", "root"])   # orphan not reachable from root

    def test_multiple_roots(self):
        nodes = [{"id": "r1", "deps": [{"pkg": "a"}]}, {"id": "r2", "deps": [{"pkg": "b"}]},
                 {"id": "a", "deps": []}, {"id": "b", "deps": []}, {"id": "orphan", "deps": []}]
        self.assertEqual(P._reachable_ids(["r1", "r2"], nodes), ["a", "b", "r1", "r2"])

    def test_empty_roots_reaches_nothing(self):
        # item C: NO all-nodes fall-back -- empty roots reach nothing (a disconnected node is
        # deliberately unreachable, so an injected orphan cannot smuggle itself in).
        nodes = [{"id": "a", "deps": []}, {"id": "b", "deps": []}]
        self.assertEqual(P._reachable_ids([], nodes), [])


class TestClassifyFetchPrecedence(unittest.TestCase):
    """follow-up item A (producer side): _classify_acquisitions returns the dependency-fetch
    terminal status even when a malformed sparse-cache entry is also present, and short-circuits
    BEFORE touching the (absent, never-computed) cache manifests of a terminal record."""

    def test_failed_fetch_with_malformed_cache_is_fetch_failure(self):
        A = {"install": {"exit": 0},
             "dependency_fetch_result": {"status": "COREUTILS_DEPENDENCY_FETCH_FAILURE"},
             "cargo_index_cache_unparseable": [{"path": "registry/index/x/.cache/li/libc"}]}
        B = {"install": {"exit": 0}, "dependency_fetch_result": {"status": "ok"}}
        cls = P._classify_acquisitions(A, B, {})
        self.assertEqual(cls["outcome"], "COREUTILS_DEPENDENCY_FETCH_FAILURE")

    def test_lock_mutation_terminal(self):
        A = {"install": {"exit": 0}, "dependency_fetch_result": {"status": "ok"}}
        B = {"install": {"exit": 0},
             "dependency_fetch_result": {"status": "COREUTILS_DEPENDENCY_FETCH_LOCK_MUTATION"}}
        cls = P._classify_acquisitions(A, B, {})
        self.assertEqual(cls["outcome"], "COREUTILS_DEPENDENCY_FETCH_LOCK_MUTATION")


