#!/usr/bin/env python3
"""Builds the Sandboy confinement policy (TOML) for the N2-A canary build.

Deliberately narrow — this is not a generic policy generator: it exists only
to express, for this one pinned dotnet build, exactly the paths/ports/env
names the accepted Sandboy commit (e925058ddea405b5821fc0aed4882c76650dcbe9)
should allow. See Own.NET/sandboy/policy.example.toml for the format this
mirrors: fs_ro, fs_rw, tcp_connect, tcp_bind, env_allow.
"""
from __future__ import annotations

import hashlib
from pathlib import Path


def _toml_str_list(paths: list[Path]) -> str:
    return "[" + ", ".join(f'"{p}"' for p in paths) + "]"


def build_policy(
    *,
    source_root: Path,
    project_dir: Path,
    sdk_root: Path,
    home_dir: Path,
    tmp_dir: Path,
    capture_out_dir: Path,
    repo_tools_dir: Path | None = None,
) -> str:
    """fs_ro: the reviewed source tree (read+execute only — the build must not
    modify anything outside its own project's bin/obj) and the pinned .NET SDK
    + minimal system libraries the dotnet host needs to exec at all. fs_rw:
    only the project's own bin/obj, a dedicated HOME/tmp, and the capture
    output directory. No network (tcp_connect/tcp_bind both empty — the outer
    network namespace already denies this at a lower layer; Sandboy denies it
    again at the per-process layer, defense in depth). env_allow: the minimal
    names dotnet actually needs, all pointed at dedicated non-host locations.
    """
    fs_ro = [
        source_root,
        sdk_root,
        Path("/usr"),
        Path("/bin"),
        Path("/lib"),
        Path("/lib64"),
        Path("/etc"),
        # CoreCLR startup reads /proc (cpuinfo/meminfo/self/*) and
        # /sys/fs/cgroup (container memory/cpu limit detection) — without
        # these, an earlier N2-A run showed CoreCLR abort with "Failed to
        # create CoreCLR, HRESULT: 0x8007000E" (E_OUTOFMEMORY), even though
        # the real cause was Landlock denying these paths, not an actual
        # memory shortage (peak RSS was ~10MB).
        Path("/proc"),
        Path("/sys"),
    ]
    if repo_tools_dir is not None:
        # Only the network_probe.py helper (invoked through the same wrapper
        # for network-isolation-report.json evidence) needs this; the real
        # build's argv never references it.
        fs_ro.append(repo_tools_dir)
    fs_rw = [
        project_dir / "bin",
        project_dir / "obj",
        home_dir,
        tmp_dir,
        capture_out_dir,
    ]
    lines = [
        "# Generated by sandboy_policy.py for the N2-A miner canary. Do not edit by hand.",
        f"fs_ro = {_toml_str_list(fs_ro)}",
        f"fs_rw = {_toml_str_list(fs_rw)}",
        "tcp_connect = []",
        "tcp_bind = []",
        'env_allow = ["PATH", "HOME", "TMPDIR", "DOTNET_ROOT", '
        '"DOTNET_CLI_TELEMETRY_OPTOUT", "DOTNET_NOLOGO", '
        '"DOTNET_SKIP_FIRST_TIME_EXPERIENCE", "DOTNET_MULTILEVEL_LOOKUP", '
        '"DOTNET_GENERATE_ASPNET_CERTIFICATE"]',
    ]
    return "\n".join(lines) + "\n"


def canonical_policy_text(text: str, work_dir: Path) -> str:
    """Replaces the per-job, per-runner work_dir prefix (a fresh temp
    directory in every capture job) with a fixed placeholder, so two
    independent capture jobs — which necessarily use different absolute temp
    paths — can still be compared for STRUCTURAL policy identity. This is the
    field `compare_reproducibility.py` checks for equality; the raw,
    job-specific policy (with real paths) is what Sandboy actually enforced,
    and is what the per-job execution receipt records."""
    return text.replace(str(work_dir), "<WORKDIR>")


def write_policy(path: Path, *, work_dir: Path, **kwargs) -> tuple[str, str]:
    """Writes the policy file and returns (raw_sha256, canonical_sha256)."""
    text = build_policy(**kwargs)
    path.write_text(text)
    raw_sha256 = hashlib.sha256(text.encode()).hexdigest()
    canonical_sha256 = hashlib.sha256(canonical_policy_text(text, work_dir).encode()).hexdigest()
    return raw_sha256, canonical_sha256
