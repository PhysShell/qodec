"""Unit tests for determinism_probe.py's pure logic (compiler-server
detection and per-variant aggregation). The actual repeated-build behavior
is proven for real on GitHub-hosted runners (n2a-determinism-probe-report.json,
generated by this same module's CLI) — these tests cover the parts that
don't require a real dotnet toolchain."""
import sys
import unittest
from pathlib import Path
from unittest import mock

TOOLS = Path(__file__).resolve().parents[1] / "tools"
sys.path.insert(0, str(TOOLS))
import determinism_probe as dp  # noqa: E402


class TestDetectCompilerServer(unittest.TestCase):
    def test_detects_compiler_server_string_marker(self):
        stdout = b"        CompilerServer: server - server processed compilation - Foo (net8.0)\n"
        with mock.patch.object(dp.subprocess, "run") as mock_run:
            mock_run.return_value = mock.Mock(stdout="")
            result = dp._detect_compiler_server(stdout, b"")
        self.assertTrue(result["string_markers_seen"]["CompilerServer: server"])
        self.assertFalse(result["string_markers_seen"]["VBCSCompiler"])

    def test_detects_vbcscompiler_string_marker(self):
        stdout = b"Starting VBCSCompiler process\n"
        with mock.patch.object(dp.subprocess, "run") as mock_run:
            mock_run.return_value = mock.Mock(stdout="")
            result = dp._detect_compiler_server(stdout, b"")
        self.assertTrue(result["string_markers_seen"]["VBCSCompiler"])

    def test_no_markers_when_absent(self):
        stdout = b"Build succeeded.\n    0 Warning(s)\n    0 Error(s)\n"
        with mock.patch.object(dp.subprocess, "run") as mock_run:
            mock_run.return_value = mock.Mock(stdout="")
            result = dp._detect_compiler_server(stdout, b"")
        self.assertFalse(result["string_markers_seen"]["CompilerServer: server"])
        self.assertFalse(result["string_markers_seen"]["VBCSCompiler"])

    def test_ps_failure_yields_none_not_a_crash(self):
        with mock.patch.object(dp.subprocess, "run", side_effect=OSError("no ps")):
            result = dp._detect_compiler_server(b"", b"")
        self.assertIsNone(result["vbcscompiler_process_seen"])


class TestVariantAggregation(unittest.TestCase):
    def _fake_capture(self, sanitized_stdout_sha256, sanitized_stderr_sha256="s", exit_code=0):
        return {
            "build_exit_code": exit_code,
            "sanitized_stdout_sha256": sanitized_stdout_sha256,
            "sanitized_stderr_sha256": sanitized_stderr_sha256,
        }

    def test_all_matching_hashes_is_deterministic(self):
        captures = [self._fake_capture("h1") for _ in range(6)]
        with mock.patch.object(dp, "run_one_capture", side_effect=captures):
            with mock.patch.object(dp.adapter, "capture_toolchain_identity",
                                    return_value={"sdk_version": "8.0.404", "runtime_identifier": "linux-x64",
                                                   "dotnet_binary_sha256": "d" * 64}):
                result = dp.run_variant(
                    source_root=Path("/tmp"), dotnet_bin="dotnet", dotnet_root="/tmp",
                    project_rel="Foo.csproj", variant_name="v", extra_argv=[], repeats=6,
                    base_argv_tail=[],
                )
        self.assertTrue(result["deterministic"])
        self.assertEqual(len(result["distinct_sanitized_stdout_hashes"]), 1)

    def test_one_divergent_hash_is_not_deterministic(self):
        captures = [self._fake_capture("h1") for _ in range(5)] + [self._fake_capture("h2")]
        with mock.patch.object(dp, "run_one_capture", side_effect=captures):
            with mock.patch.object(dp.adapter, "capture_toolchain_identity",
                                    return_value={"sdk_version": "8.0.404", "runtime_identifier": "linux-x64",
                                                   "dotnet_binary_sha256": "d" * 64}):
                result = dp.run_variant(
                    source_root=Path("/tmp"), dotnet_bin="dotnet", dotnet_root="/tmp",
                    project_rel="Foo.csproj", variant_name="v", extra_argv=[], repeats=6,
                    base_argv_tail=[],
                )
        self.assertFalse(result["deterministic"])
        self.assertEqual(len(result["distinct_sanitized_stdout_hashes"]), 2)

    def test_nonzero_exit_code_is_not_deterministic_even_if_hashes_match(self):
        captures = [self._fake_capture("h1", exit_code=1) for _ in range(6)]
        with mock.patch.object(dp, "run_one_capture", side_effect=captures):
            with mock.patch.object(dp.adapter, "capture_toolchain_identity",
                                    return_value={"sdk_version": "8.0.404", "runtime_identifier": "linux-x64",
                                                   "dotnet_binary_sha256": "d" * 64}):
                result = dp.run_variant(
                    source_root=Path("/tmp"), dotnet_bin="dotnet", dotnet_root="/tmp",
                    project_rel="Foo.csproj", variant_name="v", extra_argv=[], repeats=6,
                    base_argv_tail=[],
                )
        self.assertFalse(result["deterministic"])
        self.assertFalse(result["all_exit_codes_zero"])


if __name__ == "__main__":
    unittest.main()
